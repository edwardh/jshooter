package com.edwardh.games.jshooter.entity;

public class Asteroid {
    private int XPos;
    private int YPos;

    public void setXPos(int XPos) {
        this.XPos = XPos;
    }

    public int getXPos() {
        return XPos;
    }

    public void setYPos(int YPos) {
        this.YPos = YPos;
    }

    public int getYPos() {
        return YPos;
    }
}
